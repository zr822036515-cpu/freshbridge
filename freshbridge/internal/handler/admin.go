package handler

import (
	"net/http"
	"strconv"

	"github.com/gin-gonic/gin"

	"freshbridge/internal/model"
	"freshbridge/internal/repository"
	"freshbridge/internal/service"
)

type AdminHandler struct {
	repo    *repository.AdminRepo
	updater *service.MarketUpdater
}

func NewAdminHandler(repo *repository.AdminRepo, updater *service.MarketUpdater) *AdminHandler {
	return &AdminHandler{repo: repo, updater: updater}
}

// POST /admin-api/update-prices
func (h *AdminHandler) TriggerPriceUpdate(c *gin.Context) {
	h.updater.Update()
	c.JSON(http.StatusOK, gin.H{"ok": true, "message": "价格数据已更新"})
}

// GET /admin/dashboard
func (h *AdminHandler) Dashboard(c *gin.Context) {
	stats, err := h.repo.Dashboard()
	if err != nil {
		c.JSON(http.StatusInternalServerError, gin.H{"error": err.Error()})
		return
	}
	c.JSON(http.StatusOK, stats)
}

// GET /admin/users
func (h *AdminHandler) ListUsers(c *gin.Context) {
	page, _ := strconv.Atoi(c.DefaultQuery("page", "1"))
	pageSize, _ := strconv.Atoi(c.DefaultQuery("page_size", "20"))
	if page < 1 { page = 1 }
	items, total, err := h.repo.ListUsers(page, pageSize)
	if err != nil {
		c.JSON(http.StatusInternalServerError, gin.H{"error": err.Error()})
		return
	}
	c.JSON(http.StatusOK, gin.H{"users": items, "total": total})
}

// PUT /admin/users/:id
func (h *AdminHandler) UpdateUser(c *gin.Context) {
	id, err := strconv.ParseInt(c.Param("id"), 10, 64)
	if err != nil {
		c.JSON(http.StatusBadRequest, gin.H{"error": "invalid id"})
		return
	}
	var req struct {
		Verified *int `json:"verified"`
	}
	if err := c.ShouldBindJSON(&req); err != nil {
		c.JSON(http.StatusBadRequest, gin.H{"error": err.Error()})
		return
	}
	if req.Verified != nil {
		h.repo.UpdateUserStatus(id, *req.Verified)
	}
	c.JSON(http.StatusOK, gin.H{"ok": true})
}

